def fiz_buzz(number):
    for i in range(number):
        if i % 3 == 0 and i % 5 == 0:
            print('FizzBuzz')
        elif i % 3 == 0:
            print('Fizz')
        elif i % 5 == 0:
            print('Buzz')
        else:
            print(i)


fiz_buzz(int(input('Enter your number: ')))
